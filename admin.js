document.getElementById('snip').onclick = function(){
	  alert("Loading Snipping tool");
	  exec("SnippingTool.exe", (error, stdout, stderr) => {
    if (error) {
        console.log(`error: ${error.message}`);
        return;
    }
    if (stderr) {
        console.log(`stderr: ${stderr}`);
        return;
    }
    console.log(`stdout: ${stdout}`);
});
}

